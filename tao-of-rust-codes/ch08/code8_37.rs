fn main() {
    println!("{:?}", format!("{:.4}", 1234.5678));
    println!("{:?}", format!("{:.2}", 1234.5618));
    println!("{:?}", format!("{:.2}", 1234.5678));
    println!("{:?}", format!("{:<10.4}", 1234.5678));
    println!("{:?}", format!("{:^12.2}", 1234.5678));
    println!("{:?}", format!("{:0^12.2}", 1234.5678));
    println!("{:?}", format!("{:e}", 1234.5678));
}